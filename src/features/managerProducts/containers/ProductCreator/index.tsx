export const TextMobileStepper = () => {
  const classes = useStyles();
  const theme = useTheme();
  const [activeStep, setActiveStep] = React.useState(0);
  const maxSteps = tutorialSteps.length;

  const handleNext = () => {
      console.log(activeStep)

    if (activeStep <= maxSteps) {
      setActiveStep((prevActiveStep) => prevActiveStep = maxSteps -activeStep);
    } else {
      setActiveStep((prevActiveStep) => prevActiveStep + 3);
    }
  };

  const handleBack = () => {
    if (activeStep === 0) {
      setActiveStep((prevActiveStep) => prevActiveStep = maxSteps-3)
    } else {
      setActiveStep((prevActiveStep) => prevActiveStep - 3);
    } 
  };

  return (
    <div className={classes.root}>
      <div className={classes.divimgs}>
        <img
          className={classes.img}
          src={tutorialSteps[activeStep].imgPath}
          alt={tutorialSteps[activeStep].label}
        />
        <img
          className={classes.img}
          src={tutorialSteps[activeStep+1].imgPath}
          alt={tutorialSteps[activeStep+1].label}
        />
        <img
          className={classes.img}
          src={tutorialSteps[activeStep+2].imgPath}
          alt={tutorialSteps[activeStep+2].label}
        />
      </div>
      <MobileStepper
        steps={maxSteps}
        
        position="static"
        activeStep={activeStep}
        nextButton={

          <Button size="small" onClick={handleNext}>
            {console.log(activeStep)}

            Next
            {theme.direction === 'rtl' ? <KeyboardArrowLeft /> : <KeyboardArrowRight />}
          </Button>
        }
        backButton={
          <Button size="small" onClick={handleBack}>
            {tutorialSteps.length}
            {theme.direction === 'rtl' ? <KeyboardArrowRight /> : <KeyboardArrowLeft />}
            Back
          </Button>
        }
      />
    </div>
  );
}